void solve() {

    int N;
    cin >> N;
    
    if(N <= 9) {
        for(int i = 0; i<N; i++) {
            cout << base[N][i] << endl;
        }
        return;
    }

    vector<stringstream> ans(N);
    
    // insert base
    int m = N%6;
    int cnt = (N-num[m])/6;
    for(int i = 0; i<num[m]; i++) {
        ans[i] << base[num[m]][i];
        for(int j = 0; j<cnt; j++) {
            ans[i] << righ[m][i];
        }
    }
    
    for(int j = 0; j<cnt; j++) {
        int i = num[m]+6*j;
        for(int k = 0; k<6; k++) {
            ans[i+k] << bottom[m][k];
            for(int a = 0; a<cnt; a++) {
                ans[i+k] << fil[k];
            }
        }
    }
    
    // cout << m << " " << num[m] << " " << cnt << endl;

    for(int i = 0; i<N; i++) cout << ans[i].str() << '\n';

}

int main() {

    int T;
    cin >> T;
    for(int i = 0; i<T; i++) {
        // cout << "case " << i << ": " << endl;
        solve();
    }
    cout << flush;

}